export interface FootballClub {
  clubName: string, // club name
  clubCountry: string,
  numOfMembers: number, // number of members in club
  numOfWins: number, // number of wins
  numOfDraws: number, // number of draws
  numOfDefeats: number,
  numOfGoalsScored: number, // number of goals for club
  numOfGoalsConceded: number,
  numOfPoints: number, // number of points scored by club
  numOfMatches: number, // number of matches played by club
  goalDifference: number
}
